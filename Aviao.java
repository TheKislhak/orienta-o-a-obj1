package car;

public class Aviao {

	String coraviao;
	String marca2;
	int passageiros;
	int pilotos;

	public String getCoraviao() {
		return coraviao;
	}
	public void setCoraviao(String coraviao) {
		this.coraviao = coraviao;
	}
	public String getMarca2() {
		return marca2;
	}
	public void setMarca2(String marca2) {
		this.marca2 = marca2;
	}
	public int getPassageiros() {
		return passageiros;
	}
	public void setPassageiros(int passageiros) {
		this.passageiros = passageiros;
	}
	public int getPilotos() {
		return pilotos;
	}
	public void setPilotos(int pilotos) {
		this.pilotos = pilotos;
	}

}
