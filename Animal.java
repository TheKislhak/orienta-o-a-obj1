package car;

public class Animal {
	
	String nomeanimal;
	String raca;
	String portetamanho;
	String som;
	
	public String getNomeanimal () {
		return nomeanimal;
	}
	public void setNomeanimal (String nomeanimal) {
		this.nomeanimal = nomeanimal;
	}
	public String getRaca () {
		return raca;
	}
	public void setRaca (String raca) {
		this.raca = raca;
	}
	public String getPortetamanho () {
		return portetamanho;
	}
	public void setPortetamanho (String portetamanho) {
		this.portetamanho = portetamanho;
	}
	public String getSom () {
		return som;
	}
	public void setSom (String som) {
		this.som = som;
	}

}
